export function isStandardReaction(emoji: string): emoji is ReactionTypeEmoji["emoji"] {
  return STANDARD_REACTIONS.has(emoji);
}

export function getAckReaction(config: TelegramConfig, identity: AgentIdentity): string {
  return config.ackReaction?.trim() || identity.emoji?.trim() || "\u{1F440}";
}
